void solve()

{

    long long n, a, b;

    cin >> n >> a >> b;

    if (a + b > n - 2)

    {

        cout << -1 << "\n";

        return;

    }

    if (abs(a - b) > 1)

    {

        cout << -1 << "\n";

        return;

    }



    long long l = 1, r = n, q = (a > b);

    for (long long i = 0; i < n; i++)

    {

        if (q)

        {

            cout << l++ << " ";

        }

        else

        {

            cout << r-- << " ";

        }

        if (i + 1 <= a + b)

        {

            q ^= 1;

        }

    }

    cout << "\n";

}



