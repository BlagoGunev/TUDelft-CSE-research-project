using namespace std;
int read(){
    int xx=0,ff=1;char ch=getchar();
    while(ch>'9'||ch<'0'){if(ch=='-')ff=-1;ch=getchar();}
    while(ch>='0'&&ch<='9'){xx=xx*10+ch-'0';ch=getchar();}
    return xx*ff;
}
int N,M,a,b,v,ans;
int f[100010],g[100010];
int sx,sy,fx,fy;
int main(){
	//freopen("in","r",stdin);
	N=read(),M=read(),a=read(),b=read(),v=read();
	for(int i=1;i<=a;i++)
		f[i]=read();
	for(int i=1;i<=b;i++)
		g[i]=read();
	for(int q=read();q;q--){
		ans=(1LL<<31)-1;
		sx=read(),sy=read(),fx=read(),fy=read();
		int t=lower_bound(g+1,g+1+b,sy)-g;
		int cost1=abs(sx-fx)/v,cost2=abs(sx-fx);
		if(abs(sx-fx)%v!=0)
			cost1++;
		if(t!=b+1)
			ans=min(ans,abs(g[t]-sy)+cost1+abs(g[t]-fy));
		t--;
		if(t>=1)
			ans=min(ans,abs(g[t]-sy)+cost1+abs(g[t]-fy));
		t=lower_bound(f+1,f+1+a,sy)-f;
		if(t!=a+1)
			ans=min(ans,abs(f[t]-sy)+cost2+abs(f[t]-fy));
		t--;
		if(t>=1)
			ans=min(ans,abs(f[t]-sy)+cost2+abs(f[t]-fy));
		if(sx==fx)
			ans=min(ans,abs(sy-fy));
		printf("%d\n",ans);
	}
	return 0;
}