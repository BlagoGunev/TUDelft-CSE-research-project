var gcd(var a, var b) {

    while (a != 0)  {

        b %= a;

        swap(a, b);

    }

    return b;

}

var lcm(var a, var b) {

    return (a / gcd(a, b)) * b;

}



bool check(var a, var b, var p, var m) {

    if (p == m) {

        return true;

    }

    if (a == b && p != m) {

        return false;

    }

    var t = (p - m);

    if (t * b % (b - a) != 0) {

        return false;

    }

    if (t * b / (b - a) >= -m && t * b / (b - a) <= p) {

//        cout << "TIK\n";

        return true;

    }

    return false;

}



