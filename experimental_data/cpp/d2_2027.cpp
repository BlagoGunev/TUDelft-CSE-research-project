constexpr int md = 1e9 + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

vector<Mint> fact(1, 1);
vector<Mint> inv_fact(1, 1);

Mint C(int n, int k) {
    if (k < 0 || k > n) {
        return 0;
    }
    while ((int)fact.size() < n + 1) {
        fact.push_back(fact.back() * (int)fact.size());
        inv_fact.push_back(1 / fact.back());
    }
    return fact[n] * inv_fact[k] * inv_fact[n - k];
}

int32_t main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        int n, m;
        cin >> n >> m;
        vector<int> a(n), b(m);
        for (int i = 0; i < n; i++) cin >> a[i];
        for (int i = 0; i < m; i++) cin >> b[i];
        vector<int64_t> pref(n + 1);
        for (int i = 0; i < n; i++) pref[i + 1] = pref[i] + a[i];
        Vec<2, int64_t> f(n + 1, m + 1, 1e18);
        Vec<2, Mint> g(n + 1, m + 1, 0);
        f[0][0] = 0, g[0][0] = 1;
        Vec<3, pair<int64_t, Mint>> event(m, n + 2);
        vector<map<int64_t, Mint>> best(m);
        vector<map<int64_t, int>> cnt(m);
        for (int i = 0; i <= n; i++) {
            for (int j = 0; j < m; j++) {
                for (auto&& [x, y] : event[j][i]) {
                    if (x < 0) {
                        x = -x - 1;
                        best[j][x] -= y;
                        if (--cnt[j][x] == 0) {
                            best[j].erase(x);
                            cnt[j].erase(x);
                        }
                    } else {
                        best[j][x] += y;
                        cnt[j][x]++;
                    }
                }
                if (cnt[j].size()) {
                    auto [cost, ways] = *best[j].begin();
                    if (f[i][j] > cost) {
                        f[i][j] = cost;
                        g[i][j] = ways;
                    } else if (f[i][j] == cost) {
                        g[i][j] += ways;
                    }
                }
                if (f[i][j] == 1e18) continue;
                if (j < m && f[i][j + 1] > f[i][j]) {
                    f[i][j + 1] = f[i][j];
                    g[i][j + 1] = g[i][j];
                } else if (j < m && f[i][j + 1] == f[i][j]) {
                    g[i][j + 1] += g[i][j];
                }
                int x = upper_bound(pref.begin(), pref.end(), b[j] + pref[i]) - pref.begin();
                if (x > i + 1) {
                    int64_t cost = f[i][j] + m - j - 1;
                    event[j][i + 1].emplace_back(cost, g[i][j]);
                    event[j][x].emplace_back(-cost - 1, g[i][j]);
                }
            }
        }
        if (f[n][m] == 1e18) {
            cout << -1 << '\n';
        } else {
            cout << f[n][m] << ' ' << g[n][m] << '\n';
        }
    }
}