#include <bits/stdc++.h>

using namespace std;



int main()

{

    int t=1;

    cin>>t;

    while(t--)

    {

        int n,c=0;

        cin>>n;

        int arr[n][n];

        for(int i=0; i<n; i++)

        {

            for(int j=0; j<n; j++)

            {

                int x;

                if(i%2==0)

                    x=j;

                else

                    x=n-j-1;

                if(c%2==0)

                    arr[i][x]=c/2+1;

                else

                    arr[i][x]=n*n-c/2;

                c++;

            }

            for(auto k:arr[i])

                cout<<k<<" ";

            cout<<endl;

        }

    }



}