#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
const bool multitest = 1;
const int MA = 200005;
map<int, int> d;
int n, a[MA], res;
void solve()
{
    d.clear();
    cin >> n;
    res = 0;
    for (int i = 1; i <= n; i++) cin >> a[i], d[a[i]]++;
    for (int i = 1; i <= n; i++)
    {
        res += d[a[i]] / 3;
        d[a[i]] = 0;
    }
    cout << res << "\n";
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    if (fopen("Code.inp", "r"))
    {
        freopen("Code.inp", "r", stdin);
        freopen("Code.out", "w", stdout);
    }
    int T;
    if (multitest) cin >> T;
    else T = 1;
    while (T--) solve();
    return 0;
}
