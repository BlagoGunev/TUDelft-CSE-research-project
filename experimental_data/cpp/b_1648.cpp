void solve() {

	int n, c; cin >> n >> c;

	vector<int> a(n);

	set<int> st;

	c = 0;

	for (int i = 0; i < n; i++) {

		int t; cin >> t;

		a[i] = t;

		c = max(c, t);

	}

	sort(all(a));

	a.resize(unique(all(a)) - a.begin());

	n = a.size();

	vector<int> used(c + 1), d(c + 1);

	for (auto x : a) {

		d[x] = x;

	}

	int tmp;

	for (int i = c; i >= 1; i--) {

		if (d[i]) tmp = d[i];

		d[i] = tmp;

	}

	for (int i = 0; i < n; i++) {

		for (int j = 1; a[i] * j <= c; j++) {

			if (used[j]) continue;

			int x = d[a[i] * j];

			if (x < a[i] * (j + 1)) {

				used[j] = 1;

			}

		}

	}

	bool flag = 0;

	for (int i = 1; i <= c; i++) {

		if (used[i] && d[i] != i) {

			flag = 1;		

			break;

		}

	}

	if (flag) cout << "No" << '\n';

	else cout << "Yes" << '\n';

}

