// bin pow

int binpow(int a, int b) {

    int res = 1;

    while(b) {

        if(b & 1) res = res * a;

        a = a * a;

        b >>= 1;

    }

    return res;

}



void solve() {

    string s, t;

    cin >> s >> t;

    if (t.length() == 1 && t[0] == 'a') cout << 1 << nl;

    else {

        bool is_a = false;

        for (char item : t) {

            if (item == 'a') {

                is_a = true;

                break;

            }

        }

        if (is_a) cout << -1 << nl;

        else cout << binpow(2, s.length()) << nl;

    }

}



