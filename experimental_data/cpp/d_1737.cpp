using ll = long long;
using ld = long double; 
const int MAXN = 505;


int n, m;
ll dist[MAXN][MAXN];
int dist1[MAXN][MAXN];


void solve() {
	cin >> n >> m;
	for (int i = 0; i < n; i++) {
		for (int j = 0; j < n; j++) {
			dist[i][j] = 1e18;
			dist1[i][j] = 1e9;
		}
		dist1[i][i] = 0;
	}
	for (int i = 0; i < m; i++) {
		int a, b, c;
		cin >> a >> b >> c;
		a--, b--;
		chkmin(dist[a][b], c);
		chkmin(dist[b][a], c);
		chkmin(dist1[a][b], 1);
		chkmin(dist1[b][a], 1);
	}
	for (int k = 0; k < n; k++) {
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < n; j++) {
				chkmin(dist1[i][j], dist1[i][k] + dist1[k][j]);
			}
		}
	}
	ll ans = 1e18;
	for (int k = 0; k < n; k++) {
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < n; j++) {
				if (i == j) {
					continue;
				}
				if (dist[i][j] > (ll)1e10) {
					continue;
				}
				chkmin(ans, (dist1[i][k] + dist1[0][k] + dist1[k][n - 1] + 2) * dist[i][j]);
			}
		}
	}
	for (int i = 0; i < n; i++) {
		for (int j = 0; j < n; j++) {
			if (dist[i][j] > (ll)1e10) {
				continue;
			}
			if (i == j) {
				continue;
			} 
			chkmin(ans, (dist1[0][i] + dist1[j][n - 1] + 1) * dist[i][j]);
		}
	}
	cout << ans << '\n';
}		

 
