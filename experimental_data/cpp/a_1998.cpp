template<class T>
struct DSU{
    int p[N];
    int sz[N];
    
    inline void init(int n){
        for(int i=1;i<=n;i++){
            p[i]=i;
            sz[i]=0;
        }
    }
    
    inline int find(int x){
        if(p[x]!=x) p[x]=find(p[x]);
        return p[x];
    }
    
    inline bool is_same(int a,int b){
        int pa=find(a),pb=find(b);
        if(pa==pb) return true;
        return false;
    }
    
    inline void merge(int a,int b){
        int pa=find(a),pb=find(b);
        p[pa]=pb;
        sz[pb]+=sz[pa];
    }
};
