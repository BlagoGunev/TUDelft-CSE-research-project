struct TRIE{
    int son[N][26];
    int cnt[N][26];
    int idx;
    
    inline void init(int n){
        for(int i=0;i<=std::min(N,n*26);i++){
            for(int j=0;j<26;j++){
                son[i][j]=0;
                cnt[i][j]=0;
            }
        }
        idx=0;
    }
    
    inline void insert(std::string str){
        int p=0;
        for(char ch:str){
            int u=ch-'a';
            if(!son[p][u]) son[p][u]=++idx;
            cnt[p][u]++;
            p=son[p][u];
        }
    }
    
    inline int query(std::string str){
        int p=0,res=0;
        for(char ch:str){
            int u=ch-'a';
            res+=cnt[p][u];
            p=son[p][u];
        }
        return res;
    }
};
